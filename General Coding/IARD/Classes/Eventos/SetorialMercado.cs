        #region GetDataRow

        /// <summary>
        /// GetDataRow
        /// </summary>
        /// <param name="codigo">Código do Registro</param>
        /// <returns>SetorialMercado</returns>
        public static SetorialMercado GetDataRow(string codigo)
        {
            OracleDataReader dr = LoadDataDr(codigo);
            SetorialMercado SetorialMercado = new SetorialMercado();
            try
            {
                if (dr.Read())
                {
                    SetorialMercado.codSetorMercado = Convert.ToString(dr["A048_cd_setor"]);
                    SetorialMercado.dscSetorMercado = Convert.ToString(dr["A048_dsc_setor"]);
                    if (dr["usu_inc_alt"] != DBNull.Value && dr["usu_inc_alt"] != DBNull.Value && dr["usu_inc_alt"].ToString() != "")
                    SetorialMercado.logUsuario = Convert.ToString(dr["usu_inc_alt"]);
                }
            }
            catch (Exception ex)
            {
                if (!dr.IsClosed)
                    dr.Close();
                SetorialMercado = new SetorialMercado();
                throw (ex);
            }
            finally
            {
                if (!dr.IsClosed)
                    dr.Close();
            }
            return SetorialMercado;
        }

