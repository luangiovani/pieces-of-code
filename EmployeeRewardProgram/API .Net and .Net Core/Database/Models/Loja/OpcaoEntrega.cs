    /// </atividades>
    /// <summary>
    /// Classe para Mapeamento da Tabela OpcaoEntrega, nesta tabela serão armazenados as opções de entrega
    /// </summary>
    public class OpcaoEntrega
    {
        /// <summary>
        /// Identificador único do registro na tabela
        /// </summary>
        public Guid id { get; set; }

        /// <summary>
        /// Sequencial do registro na tabela (ordenação)
        /// </summary>
        public int sequencial { get; set; }

        /// <summary>
        /// Label para aparecer quando for selecionar a opção de entrega
        /// </summary>
        public string label { get; set; }

        /// <summary>
        /// Label para esta opção quando o colaborador estiver lendo
        /// </summary>
        public string label_colaborador { get; set; }

        /// <summary>
        /// Label para esta opção quando a loja estiver lendo
        /// </summary>
        public string label_loja { get; set; }

        /// <summary>
        /// Indica se o registro está Ativo(1) ou Inativo(0)
        /// </summary>
        public bool ativo { get; set; }

        /// <summary>
        /// Data e Hora da Criação do registro
        /// </summary>
        public DateTime data_hora_criacao { get; set; }

        /// <summary>
        /// Usuário de Criação do registro
        /// </summary>
        public string cs_colaborador_criacao { get; set; }

        /// <summary>
        /// Data e Hora da Alteração do registro
        /// </summary>
        public DateTime? data_hora_alteracao { get; set; }

        /// <summary>
        /// Usuário de Alteração do registro
        /// </summary>
        public string cs_colaborador_alteracao { get; set; }
    }
}
